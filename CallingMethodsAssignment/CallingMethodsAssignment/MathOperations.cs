using System;

namespace CallingMethodsAssignment
{
    // Class containing math methods
    public class MathOperations
    {
        // Method 1: multiply the number by 2
        public int MultiplyByTwo(int number)
        {
            return number * 2;
        }

        // Method 2: add 10 to the number
        public int AddTen(int number)
        {
            return number + 10;
        }

        // Method 3: subtract 5 from the number
        public int SubtractFive(int number)
        {
            return number - 5;
        }
    }
}
